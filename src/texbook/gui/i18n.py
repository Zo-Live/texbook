def tr(language: GuiLanguage | str, key: str, **kwargs: object) -> str:
    """Return a localized string for a GUI language."""
    try:
        resolved_language = GuiLanguage(language)
    except ValueError:
        resolved_language = GuiLanguage.zh_CN
    template = _STRINGS[resolved_language].get(key)
    if template is None:
        template = _STRINGS[GuiLanguage.zh_CN].get(key, key)
    return template.format(**kwargs)
